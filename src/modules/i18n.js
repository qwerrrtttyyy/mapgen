let currentLang = 'zh';

export const getCurrentLang = () => currentLang;
export const setCurrentLang = (lang) => { currentLang = lang; };
export const t = () => I18N[currentLang];

export const applyI18n = (cb) => {
    document.documentElement.lang = currentLang;
    document.querySelectorAll('[data-i18n]').forEach(el => { 
        const k = el.dataset.i18n; 
        if (I18N[currentLang][k]) el.textContent = I18N[currentLang][k]; 
    });
    const langBtn = document.getElementById('lang-btn');
    if (langBtn) { 
        langBtn.textContent = currentLang === 'zh' ? 'EN' : '中'; 
        langBtn.setAttribute('aria-pressed', currentLang === 'en'); 
    }
    if (cb) cb();
};
